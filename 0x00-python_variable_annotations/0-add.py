#!/usr/bin/env python3
""" type-annotated function add that takes a float a and a float b
"""


def add(a:float, b:float) -> float:
    """
    function that adds two floats
    parameters:
    a(float): the first parameter
    b(float): the secound parameter
    Returns:
    The sum of a and b
    """
    return a + b
