#!/usr/bin/env python3
"""
concat that takes a string str1 and a string str2 as arguments
and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    fuction that takes 2 strings combine them

    parameters:
    str1 (string): first parameter
    str2 (string): secound parameter

    Return:
    str: combined string
    """
    return str1 + str2
